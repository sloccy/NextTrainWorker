import { buildSchedule } from '../src/gtfs/schedule-build.js';
import { execSync } from 'child_process';
import { writeFileSync, unlinkSync, existsSync } from 'fs';

const R2_BUCKET = process.env.ARRIVALS_R2_BUCKET ?? 'nexttrainworker-arrivals';
const TMP_PATH = '/tmp/nexttrainworker-stations.bin';
const GENERATED_TS = 'src/baseline.generated.ts';

function toBase64(bytes: Uint8Array): string {
  let bin = '';
  for (let i = 0; i < bytes.length; i++) bin += String.fromCharCode(bytes[i]);
  return Buffer.from(bin, 'binary').toString('base64');
}

function emitBaselineModule(bytes: Uint8Array, version: string): string {
  const b64 = toBase64(bytes);
  return `// AUTO-GENERATED by scripts/seed-schedule.ts. Do not edit by hand.
// schedule generatedAt: ${version}
// size: ${bytes.length} bytes (${(bytes.length / 1024).toFixed(1)} KB)

const B64 = "${b64}";

const _bin = atob(B64);
const _out = new Uint8Array(_bin.length);
for (let i = 0; i < _bin.length; i++) _out[i] = _bin.charCodeAt(i);

export const BASELINE_BYTES: Uint8Array = _out;
export const BASELINE_VERSION = "${version}";
`;
}

async function main() {
  console.log('[seed] Building schedule...');
  const { generatedAt, baselineBin, stationsBin } = await buildSchedule();
  console.log(`[seed] Built — baseline=${(baselineBin.length / 1024).toFixed(1)} KB, stations=${(stationsBin.length / 1024).toFixed(1)} KB`);

  const version = String(generatedAt);

  console.log(`[seed] Writing ${GENERATED_TS}...`);
  writeFileSync(GENERATED_TS, emitBaselineModule(baselineBin, version));

  if (process.argv.includes('--skip-r2')) {
    console.log('[seed] --skip-r2 set; skipping stations upload.');
    console.log(`[seed] Done. version=${version}`);
    return;
  }

  try {
    console.log('[seed] Uploading stations.bin to R2...');
    writeFileSync(TMP_PATH, stationsBin);
    execSync(
      `npx wrangler r2 object put "${R2_BUCKET}/arrivals/stations.bin" --file "${TMP_PATH}" --remote --content-type "application/octet-stream"`,
      { stdio: 'inherit' },
    );
  } finally {
    if (existsSync(TMP_PATH)) unlinkSync(TMP_PATH);
  }

  console.log(`[seed] Done. version=${version}`);
}

main().catch(err => { console.error(err); process.exit(1); });
