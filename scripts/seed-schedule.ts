import { buildSchedule } from "../src/build/schedule.js";
import { writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const GENERATED_DIR = "src/worker/generated";
const ROUTE_IDS_PATH = "src/worker/route-ids.ts";

function b64(bytes: Uint8Array): string {
  return Buffer.from(bytes).toString("base64");
}

function emitOffsets(
  templateBin: Uint8Array,
  tripOffsets: Map<string, number[]>,
  stopOffsets: Map<string, Map<string, number[]>>,
  stopToSlug: Map<string, string>,
  version: string,
): string {
  const tB64 = b64(templateBin);

  // Build TRIP_OFFSETS entries
  const tripLines: string[] = [];
  for (const [tripId, offs] of tripOffsets) {
    const escaped = tripId.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    tripLines.push(`  ["${escaped}", new Uint32Array([${offs.join(",")}])]`);
  }

  // Build STOP_OFFSETS entries as plain nested arrays (consumed by _buildSO)
  const stopLines: string[] = [];
  for (const [tripId, inner] of stopOffsets) {
    const escapedTrip = tripId.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    const innerLines: string[] = [];
    for (const [stopId, offs] of inner) {
      const escapedStop = stopId.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
      innerLines.push(`    ["${escapedStop}", [${offs.join(",")}]]`);
    }
    stopLines.push(
      `  ["${escapedTrip}", [\n${innerLines.join(",\n")}\n  ]]`,
    );
  }

  // Build STOP_ID_TO_SLUG entries
  const slugLines: string[] = [];
  for (const [stopId, slug] of stopToSlug) {
    const escapedId   = stopId.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    const escapedSlug = slug.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    slugLines.push(`  ["${escapedId}", "${escapedSlug}"]`);
  }

  return `// AUTO-GENERATED by scripts/seed-schedule.ts — do not edit
// version: ${version}  template: ${templateBin.length}B  trips: ${tripOffsets.size}  stops: ${stopOffsets.size}

function _b64(s: string): Uint8Array {
  const bin = atob(s);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

export const TEMPLATE_BYTES: Uint8Array = _b64("${tB64}");

export const TRIP_OFFSETS: ReadonlyMap<string, Uint32Array> = new Map<string, Uint32Array>([
${tripLines.join(",\n")}
]);

function _buildSO(
  pairs: [string, [string, number[]][]][],
): ReadonlyMap<string, ReadonlyMap<string, Uint32Array>> {
  const m = new Map<string, ReadonlyMap<string, Uint32Array>>();
  for (let i = 0; i < pairs.length; i++) {
    const [tid, stops] = pairs[i];
    const inner = new Map<string, Uint32Array>();
    for (let j = 0; j < stops.length; j++) {
      inner.set(stops[j][0], new Uint32Array(stops[j][1]));
    }
    m.set(tid, inner);
  }
  return m;
}

export const STOP_OFFSETS: ReadonlyMap<string, ReadonlyMap<string, Uint32Array>> = _buildSO([
${stopLines.join(",\n")}
]);

export const STOP_ID_TO_SLUG: ReadonlyMap<string, string> = new Map<string, string>([
${slugLines.join(",\n")}
]);
`;
}

function emitStations(stationsBin: Uint8Array, version: string): string {
  const sB64 = b64(stationsBin);
  return `// AUTO-GENERATED by scripts/seed-schedule.ts — do not edit
// version: ${version}  size: ${stationsBin.length}B

function _b64(s: string): Uint8Array {
  const bin = atob(s);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

export const STATIONS_BYTES: Uint8Array = _b64("${sB64}");
`;
}

function emitRouteIds(routeIdToShortName: Map<string, string>): string {
  const entries = [...routeIdToShortName.entries()]
    .map(([k, v]) => `  "${k}": "${v}"`)
    .join(",\n");
  return `// AUTO-UPDATED by scripts/seed-schedule.ts — reflects current RTD GTFS rail routes.
// Maps raw GTFS route_id → display short name.

export const ROUTE_ID_TO_SHORT_NAME: Readonly<Record<string, string>> = {
${entries}
};
`;
}

async function main(): Promise<void> {
  console.log("[seed] Building schedule...");
  const { generatedAt, templateBin, tripOffsets, stopOffsets, stopToSlug, stationsBin, routeIdToShortName } = await buildSchedule();
  console.log(
    `[seed] Built — template=${(templateBin.length / 1024).toFixed(1)}KB` +
    `  trips=${tripOffsets.size}  stops=${stopOffsets.size}` +
    `  slugs=${stopToSlug.size}  stations=${(stationsBin.length / 1024).toFixed(1)}KB`,
  );

  const version = String(generatedAt);
  mkdirSync(join(GENERATED_DIR), { recursive: true });

  const offsetsPath = join(GENERATED_DIR, "offsets.ts");
  writeFileSync(offsetsPath, emitOffsets(templateBin, tripOffsets, stopOffsets, stopToSlug, version));
  console.log(`[seed] Wrote ${offsetsPath}`);

  const stationsPath = join(GENERATED_DIR, "stations.ts");
  writeFileSync(stationsPath, emitStations(stationsBin, version));
  console.log(`[seed] Wrote ${stationsPath}`);

  writeFileSync(ROUTE_IDS_PATH, emitRouteIds(routeIdToShortName));
  console.log(`[seed] Wrote ${ROUTE_IDS_PATH}`);

  console.log(`[seed] Done. version=${version}`);
}

main().catch((err: unknown) => { console.error(err); process.exit(1); });
